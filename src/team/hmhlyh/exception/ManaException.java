package team.hmhlyh.exception;
/**
 * 管理员异常类
 * @author 123
 *
 */
public class ManaException extends Exception {

	public ManaException() {
		super();
	}

	public ManaException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super();
	}

	public ManaException(String message, Throwable cause) {
		super(message, cause);
	}

	public ManaException(String message) {
		super(message);
	}

	public ManaException(Throwable cause) {
		super(cause);
	}
	
}
